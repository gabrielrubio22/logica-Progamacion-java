    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        int empanada = Integer.parseInt(cantEmpanada.getText());
        
        int subTotal = empanada * 3000;
        
        StringBuilder cadena = new StringBuilder();
        NumberFormat formatoMoneda = NumberFormat.getCurrencyInstance(new Locale("es", "CO"));
        
        double valor;
        double total;
        
        if(empanada > 0){
            
            if(empanada >0 && empanada <51){
                
                cadena.append("El total a pagar es: " + formatoMoneda.format(subTotal) + "\n");
            
            } else if(empanada >= 51 && empanada <= 99){

                valor = subTotal * 0.05;
                total = subTotal - valor;
                cadena.append("El total a pagar es: " + formatoMoneda.format(total) + "\n")
                        .append("Su descuento fue de 7% por comprar mas de 50 empanadas\n")
                        .append("Valor descuento: " + formatoMoneda.format(valor));

            } else if(empanada >= 100 && empanada <= 200){

                valor = subTotal * 0.1;
                total = subTotal - valor;
                 cadena.append("El total a pagar es: " + formatoMoneda.format(total) + "\n")
                        .append("Su descuento fue de 10% por comprar entre 100 y 200 empanadas\n")
                        .append("Valor descuento: " + formatoMoneda.format(valor));

            } else if(empanada >= 201 && empanada <= 300){

                valor = subTotal * 0.2;
                total = subTotal - valor;
                 cadena.append("El total a pagar es: " + formatoMoneda.format(total) + "\n")
                        .append("Su descuento fue de 20% por comprar mas de 200 y menos de 300 empanadas\n")
                        .append("Valor descuento: " + formatoMoneda.format(valor));

            } else if(empanada >= 301){

                valor = subTotal * 0.3;
                total = subTotal - valor;
                 cadena.append("El total a pagar es: " + formatoMoneda.format(total) + "\n")
                        .append("Su descuento fue de 30% por comprar mas de 300 empanadas\n")
                        .append("Valor descuento: " + formatoMoneda.format(valor));
            }
        } else{
            cadena.append("Escriba un numero de empanada mayor a 0");
        }
        
        
        factura.setText(cadena.toString());
    }//GEN-LAST:event_aceptarActionPerformed

